                <p class="lead">I augusti 2024 blev jag vald till ordförande i Sala konstförening vars huvudsakliga och viktigaste verksamhet är att driva Aguélimuseet.</p>

<p>Konstföreningen är huvudman för <a href="https://aguelimuseet.se/om-museet/">Aguélimuseet</a> i Sala. Det är uppkallat efter Ivan Aguéli (1869-1917) som föddes och växte upp i Sala. Han var en synnerligen intressant människa och konstnär och Aguélimuseet är det enda museet i världen som fokuserar på honom och hans konst.</p>

