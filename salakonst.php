<h2>Kort biografi över Ivan Aguélis liv (från min b-uppsats)</h2>

<p>John Gustaf Agelii föddes 24 maj 1869 i Sala i Västmanland. Han skulle senare byta namn
till Ivan Aguéli. I skolan gick det inte bra för den unge Aguéli. Han blev relegerad från
läroverk i Sala, Västerås, Falun, Stockholm och Visby.</p>

<p>Våren 1890 fick han tillstånd av sin far att resa till Paris. Väl i Paris landade han mitt i
modernismens genombrott och fann sig snabbt till rätta. Aguéli umgicks med
avantgardistiska konstnärer och trivdes bra. Paris blev basen för resten av hans liv.</p>

<p>Aguéli fängslades två gånger under sitt liv vilket uppmärksammades i såväl fransk som
svensk press. Första gången 1894 misstänktes han för att inhyst en anarkist. Andra gången
1900 då han avlossade skott mot ett par toreadorer vid en tjurfäktning.</p>

<p>Redan 1895 skrev Aguéli i ett brev till sin mor att han skulle vilja resa till Indien och 1899
blev det av. Aguéli älskade Indien, men det blev Egypten som skulle ta upp en stor del av
hans liv. Det blev tre resor till Egypten: september 1894 till sommaren 1895, december 1902-
oktober 1909 och slutligen december 1913-februari 1916.</p>

<p>Den enda utställningen Aguéli deltog i under sin livstid var Konstnärsförbundets utställning i
Stockholm 1912. Utställningen blev en stor succé och Aguéli fick sin första uppmärksamhet
som konstnär i hemlandet. Han fick dock aldrig uppleva för redan före vernissagen hade han
rest tillbaka till Paris.</p>

<p>I Paris, främst under 1910-talet, var Aguéli aktiv som konstkritiker och skribent. Han bidrog
till tidskriften <em>La Gnose</em> där han kanske mest kända verk är <em>L’art pur</em> (den rena konsten) som publicerades 1911. Hans konstkritik och reseskildringar kunde läsas i <em>L’Encyclopédie
Contemporaine Illustrée</em>. Aguéli var också under en tid redaktör för tidskriften <em>Il Convito</em> i
Egypten.</p>

<p>Efter sista vistelsen i Egypten anlände Aguéli i Barcelona där han 1 oktober 1917 blev påkörd
av ett tåg vid en järnvägsövergång i byn L'Hospitalet de Llobregat. Omständigheterna är än
idag inte kända, men ett vanligt antagande är att hans nedsatta hörsel gjorde att han helt enkelt inte hörde tåget komma. Så abrupt och tragiskt avslutades ett av Sveriges mest
besynnerliga livsöden.</p>

